package pedro.ProjetoJava.exerciciosRevisaoIntensivo;

public class exercicio06Intensivo {
    public static void main(String[] args) {
        int valorMax = 50;
        for (int i = 0; i < valorMax; i++) {
            if (i <= 25){
                System.out.println(i);
            }
            else{
                break;
            }
        }
    }
}
